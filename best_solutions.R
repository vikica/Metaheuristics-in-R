#' Converts a string representation of a numeric vector (originated in R output)
#' to a numeric vector.
#'
#' @param solution_string A string to convert to a numeric vector.
#' The string should contain whitespace-separated numbers arranged in rows,
#' starting with the row numbering.
#' @param numbers_in_row The number of numbers in each row of the string.
#'
#' @return A numeric vector containing the numbers in the input string.
#'
#' @examples
#' string_to_vector("[1,]  1.0000000000 -1.0000000000
#'                   [2,]  1.0000000000 -1.0000000000
#'                   [3,]  1.0000000000 -1.0000000000
#'                   [4,]  1.0000000000 -1.0000000000")
string_to_vector <- function(solution_string, numbers_in_row) {
  data_split <- unlist(strsplit(solution_string, "[ \n]+"))
  data_split <- data_split[!(seq_along(data_split) %% (numbers_in_row+1) == 1)]
  data_vector <- as.numeric(data_split)
  return (data_vector)
}


